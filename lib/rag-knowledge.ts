// RAG 검색 함수
export function searchKnowledge(curriculum: string, area: string, outputType: string): string {
  let knowledge = ''

  // 커리큘럼 지식 추가
  if (curriculum === 'standard-v4') {
    knowledge += STANDARD_CURRICULUM_KNOWLEDGE + '\n\n'
  } else if (curriculum === 'nuri-2019') {
    knowledge += NURI_CURRICULUM_KNOWLEDGE + '\n\n'
  }

  // 출력 유형별 가이드 추가
  if (outputType === '배움읽기') {
    knowledge += LEARNING_LOG_GUIDE + '\n\n'
  } else if (outputType === '배움지원') {
    knowledge += NEXT_STEPS_GUIDE + '\n\n'
  }

  return knowledge
}
